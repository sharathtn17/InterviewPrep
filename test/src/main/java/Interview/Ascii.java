package Interview;

public class Ascii {
	
    public static void main(String[] args) {
        Character c = 'A';
        int value = c;
        //1 : Assigning
        System.out.println("Ascii : "+value);
        //2 : Typecasting
        System.out.println("Ascii : "+(int)c);
        System.out.println("Ascii : "+(char)value);
    }


}
